	MOV TMOD,#01H
	MOV TL0, #00H
	MOV TH0, #00H
	SETB P1.5
	SETB TR0
	
HERE1:	JNB TF0, HERE1
	CLR TR0
	CLR TF0
	CLR P1.5
	RET
	
